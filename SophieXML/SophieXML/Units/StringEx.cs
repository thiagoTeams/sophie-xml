        public static string RandomId()
        {
            return Guid.NewGuid().ToString().ToUpper();
        }

        public static string FirstToUpper(this string defaultValue)
        {
            if (string.IsNullOrWhiteSpace(defaultValue) || string.IsNullOrEmpty(defaultValue))
                return defaultValue;

            return new StringBuilder(defaultValue.Substring(0, 1).ToUpper()).Append(defaultValue.Substring(1).ToLower()).ToString();
        }

        public static bool IsValidEmail(string email)
        {
            try
            {
                if (string.IsNullOrWhiteSpace(email))
                    return false;

                try
                {
                    // Normalize the domain
                    email = Regex.Replace(email, @"(@)(.+)$", DomainMapper, RegexOptions.None, TimeSpan.FromMilliseconds(200));

                    // Examines the domain part of the email and normalizes it.
                    string DomainMapper(Match match)
                    {
                        // Use IdnMapping class to convert Unicode domain names.
                        var idn = new IdnMapping();

                        // Pull out and process domain name (throws ArgumentException on invalid)
                        string domainName = idn.GetAscii(match.Groups[2].Value);

                        return match.Groups[1].Value + domainName;
                    }
                }
                catch (RegexMatchTimeoutException)
                {
                    return false;
                }
                catch (ArgumentException)
                {
                    return false;
                }

                try
                {
                    return Regex.IsMatch(email, @"^[^@\s]+@[^@\s]+\.[^@\s]+$", RegexOptions.IgnoreCase, TimeSpan.FromMilliseconds(250));
                }
                catch (RegexMatchTimeoutException)
                {
                    return false;
                }
            }
            catch
            {
                return false;
            }
        }

        public static bool IsValidPhone(string number)
        {
            try
            {
                return Regex.Match(number, @"^\(?([0-9\(\)\/\+ \-]*)$").Success && number.Substring(0, 1) == "+" && number.Length == 12;
            }
            catch
            {
                return false;
            }
        }

        public static bool IsValidUsername(string username)
        {
            return !string.IsNullOrEmpty(username) && username.Length > 6;
        }

